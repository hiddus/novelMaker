def generate_质量检查清单(章节规划: dict) -> list[str]:
    """生成质量检查清单"""
    清单 = []

    for 维度, 标准 in QUALITY_STANDARDS.items():
        清单项 = f"{维度}（最低分{标准['min_score']}）："
        for 准则 in 标准['criteria']:
            清单项 += f"\n  - {准则}"
        清单.append(清单项)

    return 清单


