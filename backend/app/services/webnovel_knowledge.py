def generate_爽点指导(爽点类型: 爽点类型, 上下文: dict) -> str:
    """生成爽点写作指导"""
    爽点信息 = 爽点库.get(爽点类型, {})
    if not 爽点信息:
        return ""

    指导 = f"""
爽点类型：{爽点类型}
回报强度：{爽点信息.get('回报强度', '中')}
触发条件：{', '.join(爽点信息.get('触发条件', []))}
典型表现：{', '.join(爽点信息.get('典型表现', []))}

写作要点：
"""
    for 要点 in 爽点信息.get('写作要点', []):
        指导 += f"- {要点}\n"

    return 指导


def generate_节奏指导(节奏类型: 节奏类型, 上下文: dict) -> str:
    """生成节奏写作指导"""
    节奏信息 = 节奏模板.get(节奏类型, {})
    if not 节奏信息:
        return ""

    指导 = f"""
节奏类型：{节奏类型}
适用场景：{', '.join(节奏信息.get('适用场景', []))}
转折密度：{节奏信息.get('转折密度', '>=2个转折')}
字数要求：{节奏信息.get('字数要求', '2000-3000字')}

结构设计：
"""
    结构 = 节奏信息.get('结构', {})
    for 部分, 描述 in 结构.items():
        指导 += f"- {部分}: {描述}\n"

    指导 += "\n写作要点：\n"
    for 要点 in 节奏信息.get('写作要点', []):
        指导 += f"- {要点}\n"

    return 指导


